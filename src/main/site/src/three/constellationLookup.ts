/**
 * Return the constellation name for J2000.0 equatorial coordinates.
 *
 * @param ra_deg  Right ascension in degrees [0, 360)
 * @param dec_deg Declination in degrees [-90, +90]
 * @returns Full English constellation name, e.g. "Virgo"
 */
export function getConstellation(ra_deg: number, dec_deg: number): string {
  const [ra1875, dec1875] = precessJ2000toB1875(ra_deg, dec_deg)
  // Convert RA from degrees to hours for boundary table comparison
  const raHours = ra1875 / 15.0

  for (const [raLow, raHigh, decLow, abbr] of BOUNDARIES) {
    if (dec1875 >= decLow && raHours >= raLow && raHours < raHigh) {
      return CONSTELLATION_NAMES[abbr] ?? abbr
    }
  }

  // Fallback — should never be reached with a complete boundary table
  return 'Unknown'
}
