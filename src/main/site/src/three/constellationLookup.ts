/**
 * IAU constellation lookup using the Roman 1987 boundary algorithm.
 * Boundary table is defined at epoch B1875.0; input J2000.0 coordinates
 * are precessed to B1875.0 before the table scan.
 *
 * Reference: Roman, N.G. 1987, PASP 99, 695
 */

const DEG2RAD = Math.PI / 180
const RAD2DEG = 180 / Math.PI

/**
 * Precess equatorial coordinates from J2000.0 to B1875.0 using
 * Lieske et al. (1977) rotation matrices.
 */
function precessJ2000toB1875(ra_deg: number, dec_deg: number): [number, number] {
  const ra = ra_deg * DEG2RAD
  const dec = dec_deg * DEG2RAD
  // t = Julian centuries from J2000.0 to B1875.0 = -1.24928
  const t = -1.24928
  const T = 0.0
  const zetaA = ((2306.2181 + 1.39656 * T - 0.000139 * T * T) * t
    + (0.30188 - 0.000344 * T) * t * t
    + 0.017998 * t * t * t) / 3600.0 * DEG2RAD
  const zA = ((2306.2181 + 1.39656 * T - 0.000139 * T * T) * t
    + (1.09468 + 0.000066 * T) * t * t
    + 0.018203 * t * t * t) / 3600.0 * DEG2RAD
  const thetaA = ((2004.3109 - 0.85330 * T - 0.000217 * T * T) * t
    - (0.42665 + 0.000217 * T) * t * t
    - 0.041775 * t * t * t) / 3600.0 * DEG2RAD
  const A = Math.cos(dec) * Math.sin(ra + zetaA)
  const B = Math.cos(thetaA) * Math.cos(dec) * Math.cos(ra + zetaA) - Math.sin(thetaA) * Math.sin(dec)
  const C = Math.sin(thetaA) * Math.cos(dec) * Math.cos(ra + zetaA) + Math.cos(thetaA) * Math.sin(dec)
  const newRa = (Math.atan2(A, B) + zA) * RAD2DEG
  const newDec = Math.asin(C) * RAD2DEG
  const raNorm = ((newRa % 360) + 360) % 360
  return [raNorm, newDec]
}

